#include "util.h"
#include "log.h"

int parseInteger(const char* intStr, int defaultValue) {
  if (intStr) {
    return atoi(intStr);
  }
  return defaultValue;
}
void splitInteger(int64_t duration, int count, int64_t *result) {
  if (duration < count) return;
  if (!result) return;

  int i = 0;
  int64_t current = 0;
  int64_t increment = duration / count;
  for (; i<count; ++i) {
    result[i] = current;
    current += increment;
  }
}
